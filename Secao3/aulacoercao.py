# conversão de tipos, coerção
# type convertion, typecasting, coercion
# é o ato de converter um tipo em outro
#  tipos imutáveis e primitivos:
# str, int, float, bool

print(int('1'), type(int('1'))) # valor e seu tipo   
print(type(float('1') + 1), float('1'), float('1')+4) # converte a string 1 em número inteiro e mostra seu tipo 
print(bool(' '))
print(str(11) + 'b') # Converte 11 (numero inteiro) em string e concatena com b